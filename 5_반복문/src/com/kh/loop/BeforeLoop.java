package com.kh.loop;

public class BeforeLoop {

	public static void main(String[] args) {
		// 실행용 메소드
		
		// 1회 반복
		System.out.println("HelloWorld");
		
		// 5회 반복
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		
		// 10만 번 반복
		
		for(int i = 0; i < 100000 ; i++) {
			System.out.println("Hello World");
		}
		
		
	}

}
